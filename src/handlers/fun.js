// ============================================================
// 藏头诗 — AI 生成
// ============================================================
export async function getAcrosticPoem(words) {
  if (!words || words.length < 2) return '请提供至少2个关键字，如"藏头诗 春风"。';
  try {
    const result = await chat(
      `请以"${words}"每字为句首，创作一首七言藏头诗。只输出诗句，每句一行，不要解释。`,
      '你是一个古典诗词创作助手。严格按要求创作藏头诗，只输出诗句。'
    );
    return `📜 藏头诗 · ${words}\n${result}`;
  } catch {
    return '藏头诗生成失败，请稍后重试。';
  }
}

// ============================================================
// 今天的梗 — AI 生成
// ============================================================
export async function getDailyMeme() {
  const now = new Date();
  const dateStr = `${now.getMonth() + 1}月${now.getDate()}日`;
  try {
    const result = await chat(
      `今天是${dateStr}。请生成一个简短有趣的网络热梗或搞笑段子，控制在50字以内，贴合当天日期/时事。`,
      '你是一个段子手，了解最新网络梗文化。只输出梗内容本身，不要加任何前缀和表情包描述。'
    );
    return `💥 今日梗图 · ${dateStr}\n${result}`;
  } catch {
    return '💥 今天的梗还在加载中……';
  }
}
